type AdminNavProps = { unreadSubmissions?: number };

export function AdminNav({ unreadSubmissions = 0 }: AdminNavProps) {
  const pathname = usePathname();

  return (
    <nav className="flex-1 px-3 py-4 space-y-0.5 overflow-y-auto">
      {navItems.map((item) => {
        const isActive =
          item.href === "/admin"
            ? pathname === "/admin"
            : pathname.startsWith(item.href);
        const showBadge = item.badge && unreadSubmissions > 0;
        return (
          <Link
            key={item.href}
            href={item.href}
            className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
              isActive
                ? "bg-primary-600 text-gray-900"
                : "text-zinc-300 hover:bg-zinc-800 hover:text-white"
            }`}
          >
            <NavIcon name={item.icon} />
            <span className="flex-1">{item.label}</span>
            {showBadge && (
              <span className="min-w-[1.25rem] rounded-full bg-amber-500 px-1.5 py-0.5 text-xs font-semibold text-white">
                {unreadSubmissions > 99 ? "99+" : unreadSubmissions}
              </span>
            )}
          </Link>
        );
      })}
    </nav>
  );
}
